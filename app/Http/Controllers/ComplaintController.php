     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Complaint::create([
            'user_id' => $request->user_id,
            'nik' => $request->nik,
            'isi_laporan' => $request->isi_laporan,
            'foto'  => $request->foto,
            'status' => 'new'
        ]);


        return  redirect (route('pengguna.index'));
    }

